//Write a program to print numbers form 0-20

#include <stdio.h>

int main() {
    printf("Numbers from 0 to 20:\n");
    
    // Printing numbers from 0 to 20 using a for loop
    for (int i = 0; i <= 20; i++) {
        printf("%d ", i);
    }
    
    printf("\n");
    
    return 0;
}
